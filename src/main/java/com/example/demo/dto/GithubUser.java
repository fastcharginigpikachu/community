package com.example.demo.dto;

public class GithubUser {
    private String name;
    private Long id;
    private String bie;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getBie() {
        return bie;
    }

    public void setBie(String bie) {
        this.bie = bie;
    }
}
